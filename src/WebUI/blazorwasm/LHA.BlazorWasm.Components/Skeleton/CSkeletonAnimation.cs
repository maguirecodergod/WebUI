namespace LHA.BlazorWasm.Components.Skeleton;

/// <summary>
/// Defines the animation type for the Skeleton component.
/// </summary>
public enum CSkeletonAnimation
{
    /// <summary>
    /// A smooth fading opacity pulse effect.
    /// </summary>
    Pulse,

    /// <summary>
    /// A shimmering gradient moving linearly across the skeleton.
    /// </summary>
    Wave,

    /// <summary>
    /// No animation (static background).
    /// </summary>
    None
}
