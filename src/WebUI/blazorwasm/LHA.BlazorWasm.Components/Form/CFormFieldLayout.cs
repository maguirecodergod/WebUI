namespace LHA.BlazorWasm.Components.Form;

/// <summary>
/// Defines the layout mode for a <see cref="FormField"/> component.
/// </summary>
public enum CFormFieldLayout
{
    /// <summary>
    /// 0 - Vertical: Label above the control (default).
    /// </summary>
    Vertical,

    /// <summary>
    /// 1 - Horizontal: Label to the left, control to the right.
    /// </summary>
    Horizontal
}
